# If a runner runs 10 miles in 30 minutes and 30 seconds,
# What is their average speed in kilometers per hour?
# (Tip: 1 mile = 1.6 km)
run_time_in_seconds = 30 * 60 + 30
print(run_time_in_seconds)
distance = 10 * 1.6
print(distance)
kilometer_time = run_time_in_seconds / distance
print(kilometer_time) 