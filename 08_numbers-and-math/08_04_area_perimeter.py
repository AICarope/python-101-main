# Write code to display the area and perimeter
# of a rectangle that has a width of 2.4 and a height of 6.4.
width = float(input("Enter the rectangule width: "))
length = float(input("Enter the rectangle length: "))
area = width * length
perimeter = (width + length) *2
print("Area is: %.1f" %area )
print("Perimeter is: %.1f" %perimeter)
